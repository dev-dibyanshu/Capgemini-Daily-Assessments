using System;

class Calculator
{
    public static void Subtract(int num1 , int num2)
    {
        return num1-num2;
    }
    public static void Mul(int num1 , int num2)
    {
        return num1*num2;
    }
    public static void Divide(int num1 , int num2)
    {
        return num1/num2;
    } 
}